export function useTranslation() {
  const language = useLanguageStore((s) => s.language)
  
  const t = (key: string, defaultValue?: string) => {
    return translations[language][key] || defaultValue || key
  }
  
  return { t, language, setLanguage: useLanguageStore.getState().setLanguage }
}

export function translateConcept(concept: string, t: (key: string, fallback: string) => string): string {
  if (!concept) return concept
  const normalized = concept
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '') // remove accents
    .replace(/[^a-z0-9]+/g, '_')     // replace spaces/symbols with _
    .trim()
  const key = `fiscalidad.concept.${normalized}`
  return t(key, concept)
}

